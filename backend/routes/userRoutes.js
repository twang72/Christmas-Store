import express from 'express';
import bcrypt from 'bcryptjs';
import expressAsyncHandler from 'express-async-handler';
import User from '../models/userModel.js';
import { generateToken } from '../utils.js';

const userRouter = express.Router();

//expressAsyncHandler can catch error in the async function inside it. the error is handled in server.js
userRouter.post(
  '/signin',
  expressAsyncHandler(async (req, res) => {
    const user = await User.findOne({ email: req.body.email }); //find user based on email
    if (user) {
      //if user exists, then check password. (plain password, encrypted password)
      if (bcrypt.compareSync(req.body.password, user.password)) {
        res.send({
          _id: user._id,
          name: user.name,
          email: user.email,
          isAdmin: user.isAdmin,
          token: generateToken(user),
        });
        return; //stop after sending data.
      }
    }
    res.status(401).send({ message: 'Invalid email or password' });
  })
);

export default userRouter;
